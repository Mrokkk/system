#include <kernel/process.h>

/*===========================================================================*
 *                                 sys_getpid                                *
 *===========================================================================*/
int sys_getpid() {
    return process_current->pid;
}

/*===========================================================================*
 *                                sys_getppid                                *
 *===========================================================================*/
int sys_getppid() {
    return process_current->ppid;
}
